CREATE TABLE Intrebare(
    id_i NUMBER(4,0) NOT NULL,--intrebarea 1, 2, 3... 
	CONSTRAINT id_i_pk PRIMARY KEY(id_i), 
    intrebaree VARCHAR2(300) NOT NULL,--textul intrebarii
    poza BLOB,
    sectiune VARCHAR2(30) NOT NULL,--de ex 'viteze'
    categorie VARCHAR2(30) NOT NULL,--B , A
	CONSTRAINT poza_ck CHECK ((poza IS NULL AND sectiune!='indicatoare si marcaje') OR poza IS NOT NULL)
);
CREATE TABLE Raspuns(
	id_i NUMBER(4,0) NOT NULL,
	CONSTRAINT id_i_raspuns_fk FOREIGN KEY (id_i) REFERENCES Intrebare(id_i),
    id_r VARCHAR2(1) NOT NULL,
	CONSTRAINT raspuns_pk PRIMARY KEY(id_i,id_r),
    raspuns VARCHAR2(30) NOT NULL,
	CONSTRAINT id_r_ck CHECK (id_r IN ('a','b','c','d')),
	corect VARCHAR2(1) NOT NULL,
	CONSTRAINT corect_ck CHECK (corect IN ('D','N'))
);
CREATE TABLE Chestionar(
	id_c NUMBER(4,0) NOT NULL PRIMARY KEY,
	denumire VARCHAR2(30) NOT NULL,
	punctaj_max NUMBER(4,0) NOT NULL,
	CONSTRAINT punctaj_max_ck CHECK (punctaj_max>=10 AND punctaj_max<=26)
);
CREATE TABLE Intrebari_Chestionar(
	id_c NUMBER(4,0) NOT NULL,
	CONSTRAINT id_c_intr_chst_fk FOREIGN KEY (id_c) REFERENCES Chestionar(id_c),
	id_i NUMBER(4,0) NOT NULL,
	CONSTRAINT id_i_intr_chst_fk FOREIGN KEY (id_i) REFERENCES Intrebare(id_i),
	CONSTRAINT intr_chst_pk PRIMARY KEY(id_i,id_c),
	nr_ordine NUMBER(4,0)
);
CREATE TABLE Test(
	id_t NUMBER(4,0) NOT NULL,
	CONSTRAINT id_t_pk PRIMARY KEY(id_t),
	dataa DATE NOT NULL,
	punctaj NUMBER(4,0) NOT NULL,
	CONSTRAINT id_c_intr_chst_tst_fk FOREIGN KEY (id_c) REFERENCES Chestionar(id_c),
	id_c NUMBER(4,0) NOT NULL
);
CREATE TABLE Raspuns_Test(
	id_t NUMBER(4,0) NOT NULL,
